            <h1>¿De qué habla el teorema de Napoleón?</h1>
            <P>
                <div class=lados10 id="quees">
            <b>Si sobre los lados 
            de un triángulo cualquiera se construyen triángulos equiláteros entonces los 
            centros de estos triángulos son también vértices de un triángulo equilátero.</b></div><br><BR>
            <CENTER><img src="./images/progresion10img/TeoNa.gif" WIDTH="30%" id="ejemplo"></CENTER> <BR><BR>
Los triángulos pueden ser tanto internos como externos, los centros de esos triángulos 
siempre formarán un triángulo equilátero, y si restamos las áreas del triángulo externo 
con la del triángulo interno, darán como resultado el área del triángulo original.<br><br>
Pero ¿por qué lo llamamos “Teorema de Napoleón”? ¿No fue Napoleón un político y militar? 
¿Por qué se nombraría a un teorema relacionado a la geometría como si lo hubiera creado él? <br><br>

Bueno, a pesar de que Napoleón no fue un matemático de la antigüedad, sino que fue un militar, 
él mostraba un gran interés en el área de las matemáticas, algo que se puede observar en una de 
las multiples cartas de este, la cual decía “El progreso y la perfección de las matemáticas están 
íntimamente ligadas a la prosperidad del estado” pues también era quien tenía amistad con algunos 
matemáticos como Laplace y Fourier, siendo Napoleón quien llegaba a proponerles problemas como el 
de dividir una circunferencia en cuatro partes iguales solamente con un compás, agregando el encontrar 
su centro, al ya explicarles lo anterior podemos llegar al porqué se le conoce como teorema de Napoleón, 
esto es porque como mencionamos hace unos instantes, Napoleón le proponía problemas matemáticos a sus 
amigos matemáticos, así que un día él les propuso un problema, el cual decía que al trazar triángulos 
equiláteros en cada lado de cualquier triángulo, los centros de esos triángulos también darían un 
triángulo equilátero, esto lo sabemos gracias al diario “The ladies diary”, donde tuvo una mención 
en un apartado, diciendo que dicho problema fue propuesto por Napoleón a sus amigos, con esto podemos 
entender el porque lo llaman “Teorema de Napoleón”, no sabemos con exactitud si fue o no una creación 
de Napoleón pero lo que si podemos confirmar es que el nombre se le quedo a partir de esa mención en 
el diario de las damas, muchos afirman que Napoleón fue quien podría haberlo creado, mientras que otros
 cuestionan el que él no contaba con los conocimientos necesarios para plantear dicho problema y que se 
 baso a partir de libros de la epoca, donde algunos matematicos proponian algo similar.<br><br>

Existen varios rumores sobre que el autor fue otro matemático de la época cercano a Napoleón, 
el cual contaba con los conocimientos necesarios para resolverlo, sospechando que antes de 
plantearlo a cercanos él ya sabía la respuesta, hablamos de William Rutherford, aunque volviendo 
a lo anterior, no hay nada que confirme quien fue su verdadero autor.<br><br>

Este teorema se originó gracias a la pregunta de ¿qué pasaría si en lugar de usar 
cuadrados usamos triángulos en el teorema de Pitágoras?, o eso es lo que creemos.<br><br>
Hay varias formas de demostrar el teorema de Napoleón, una forma sencilla y rápida es 
al ya tener nuestro triángulo formado por los centros de los triángulos, digamos 
que es nuestro triángulo MNL, el  lado MN, lo giramos 30° en sentido de las agujas 
del reloj, y con ello hacemos una homotecia de raíz cuadrada de tres con centro en A, 
con homotecia con centro nos referimos a que es una ampliación, la razón indica cuánto 
se va a ampliar, tomamos la figura y por cada punto trazamos una línea que une el 
centro de la homotecia con el punto, se mide la distancia y se multiplica por la razón 
de la homotecia, obteniendo un punto, si hacemos eso con todos los puntos obtendremos 
una copia ampliada de la figura.<br><br>
Bueno, al momento de girar el lado MN y hacer la homotecia, obtenemos la línea de segmento 
 CZ, y después haremos lo mismo con el lado LN, pero esta vez lo giraremos en el sentido 
 contrario de las agujas del reloj, y hacemos la homotecia ahora con respecto al punto B, y 
 queda exactamente lo mismo que antes, el giro y la homotecia en cada caso son equivalentes, 
 eso significa que tienen la misma simetría espiral y si contamos con giro y homotecias 
 equivalentes que nos dan el mismo segmento, significa que los segmentos de partida eran 
 iguales, por ende son del mismo tamaño, y si vemos cómo hemos obtenido los giros, tenemos 
 que el ángulo que forman ambos lados es de 60°, por ello es un triángulo equilátero.<br><br>

 Hay varias variantes de este teorema por ejemplo el teorema de Petr-Douglas-Neumann, el 
 cual plantea que sean triángulos isósceles (dos lados iguales y uno diferente) agregando que 
 existe el de Jha Savaran que plantea el problema a partir de un hexágono.


