        <h3 id="descrM">Primero una breve descripción de cada uno.</h3>
        <br>
        <b>Sustitución:</b>
        <br>
        Consiste en aislar una variable de la ecuación en función de la otra.
        Luego se reemplaza ese valor en la ecuación original para despejar la variable que quedaba.
        Se reducen las incógnitas reemplazando el valor de una variable.
        <br><br>
        <b>Igualación:</b>
        <br>
        Se basa en aplicar operaciones a ambos lados de la ecuación para ir eliminando términos.
        El objetivo es igualar a cero un lado de la ecuación aislando una variable.
        Se van igualando ambos lados haciendo lo mismo a izquierda y derecha.
        <br><br>
        <b>Reducción:</b>
        <br>
        Consiste en combinar y reducir términos semejantes en cada lado de la ecuación.
        Se agrupan las variables (x) (y) las variables y por separado.
        Reduce la ecuación a una expresión más simple con una sola variable.
        <br><br>
        <b>En resumen:</b>
        Sustitución: reemplazar el valor de una variable.
        Igualación: iguala a cero los términos con una variable.
        Reducción: combina y reduce términos semejantes.
