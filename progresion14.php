            <h1 id="ine"> INECUACIONES </h1>
            <P>Al hablar de inecuaciones nos referimos <b>una diferencia</b>, puesto que cuentan con 
                una comparación, no como en las ecuaciones donde se cuenta con un signo de igualación, 
                entre las inecuaciones se encuentran varios ejemplos: <br><br>
                1.- x+y&lt;3<br>
                2.- 2x²-2x > 0<br>
                3.- 10y-2y²/2 < 5 <br>
                4.- 2x - 3y/2 < 0<br>
                5.- 7x + 3y > 12y/5<br>
<BR>
<p id="ejemploIne">Para resolver una inecuación primero se necesita despejar una incógnita, en este caso utilizaremos 
la expresión “x+y&lt3”, al despejar la “y” se pasa al otro lado, todo lo que le impida estar sola 
convirtiéndolo a su opuesto, con esto nos referimos a que si es una suma se pasa restando, si es una 
resta se pasa sumando, si es una división se hará una multiplicación, etc; como en este caso la “x” 
se encuentra sumando a la “y” entonces se pasará al lado del 3 como negativa, dando un resultado de 
y= 3-x.</p><br>
