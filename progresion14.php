            <h1 id="MS">MÉTODO DE SUSTITUCIÓN</h1>
            <P>
            El método de sustitución como su nombre indica se basa en 
            sustituir los valores en las ecuaciones, para ello primero 
            necesitamos despejar una letra de la ecuación, de ese modo 
            comenzamos a sustituir valores.</p><br>

            Para dejar claro el método se presentan tres ejemplos para explicar de mejor 
            manera el cómo elaborarlo.<br><br id="ES1">

Antes de comenzar a despejar y sustituir valores, debemos identificar cual 
es nuestra ecuación 1 y 2, para no confundirnos después.
1.- 2x + 3y= 12<br>
2.- 4x + y= 14<br><br>
Al momento de elegir una letra para despejar se recomienda ir por la más 
sencilla, entonces, en lugar de ir por la x del 4x, iremos por la del 2x, 
si hay letras sin números como en el caso de la "y" es mejor ir por ellas 
primero.<br><br>
Despejamos "y" de la ecuación 2:<br>
4x + y= 14<br>
y= 14-4x<br><br>
Al tener un valor de y, lo podremos sustituir dentro de la otra ecuación,
como despejé en la segunda sustituiré en la primera:<br>
2x + 3y= 12<br>
2x + 3(14-4x)= 12<br><br>
De esta manera ya podemos comenzar a resolver la ecuación:<br>
2x + 3(14-4x)= 12<br>
2x + 42 - 12x= 12<br>
-10x + 42= 12<br>
-10x= 12 - 42<br>
-10x= -30<br>
x= <math class="tamfrac"><mfrac><mn>-30</mn>
            <mn>-10</mn></mfrac></math><br>
x=3<br><br>
Ahora reemplazamos valores en cualquiera de las dos ecuaciones:<br>
2x + 3y = 12<br>
2(3) + 3y=12<br>
6 + 3y = 12<br>
3y= 12-6<br>
3y= 6<br>
y= <math class="tamfrac"><mfrac><mn>6</mn>
            <mn>3</mn></mfrac></math><br>
y= 2<br><br>
Ahora sabemos que “x” vale 3 y “y” vale 2.<br><br>
Haremos la comprobación de dicho resultado:<br>
2x + 3y= 12<br>
4x + y= 14<br><br>

2(3) + 3(2)= 12<br>
4(3) + 2= 14<br><br>

6 + 6  = 12<br>
12 + 6 = 14<br><br>



