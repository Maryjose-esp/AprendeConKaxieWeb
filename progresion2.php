            <h3 id="FacTri">Factorización de un trinomio de la forma (ax² ± bx + c)</h3>
            
            <P><div class=P2Term> Este tipo de factorización solo se utiliza cuando tenemos 
                trinomio en su forma ax² ± bx + c.<br><br>
Se resuelve multiplicando “a” por cada uno de sus factores, de esta manera conseguiremos el 
primer factor de la factorización con la multiplicación de binomios, para el segundo término de 
cada binomio se utilizará el signo de “bx” y “c” respectivamente, luego dividiéndolo por la “a” y 
obteniendo la multiplicación de binomios;  para mejor explicación lo haremos paso a paso.</div></p><br>
Comenzaremos utilizando la expresión: 6x²+10x+4.<br><br>
Como mencione anteriormente se multiplicarán los factores por “a” y se dividirán por la misma, el valor 
actual de “a” es 6, dado esta situación se multiplicará cada factor por 6, y se dividirá por el mismo.<br><br>
<math class="tamfrac"><mfrac><mn>6 (6x²+10x+4)</mn>
            <mn>6</mn></mfrac></math><br><br>
El objetivo es que 6x² quede (6x) y también el segundo término.<br><br>
A los factores que cuenten con una literal se mantendrán con la multiplicación 
mientras que los que no la tienen sí se aplicará la operación.<br><br>
<b>Dicho esto, quedarían de esta manera:<br></b>
6*6x2= (6x)2 Queremos dejar indicada la multiplicación, esto lo hacemos así, 
6x6 es 36 que también se puede interpretar como 6 al cuadrado, de tal manera que quedaría 6² * x², 
así que podemos reducirlo e interpretarlo como (6x)²<br><br>
6*10x= 10*6*x = 10(6x) Primero interpretaremos que 10 es multiplicado por 6 el cual también 
es multiplicado por x, dando así que juntaremos dos términos (los que hagan que tengamos términos 
semejantes en la misma línea, en este caso 6 y “x”) mientras se multiplican por 10, quedando 
finalmente 10(6x).<br><br>
6*4= 24<br><br>
Los términos se verán de esta forma:<br><br>
<math class="tamfrac"><mfrac><mn>(6x)²+10(6x)+24</mn>
            <mn>6</mn></mfrac></math><br><br>
Ya que se tiene la nueva expresión vamos a factorizar.<br><br>
Vamos a colocar dos paréntesis divididos entre 6<br><br>
<math class="tamfrac"><mfrac><mn>(    )(    )</mn>
            <mn>6</mn></mfrac></math><br><br>
En esos paréntesis van los dos 6x<br><br>
<math class="tamfrac"><mfrac><mn>(6x   )(6x    )</mn>
            <mn>6</mn></mfrac></math><br><br>
En el primer paréntesis se coloca el signo del segundo término y en el segundo paréntesis es la 
multiplicación del signo del segundo término por el signo del tercer término.<br><br>
<math class="tamfrac"><mfrac><mn>(6x+  )(6x+    )</mn>
            <mn>6</mn></mfrac></math><br><br>
Los siguientes números que agregaremos para factorizar la expresión serán los que multiplicados 
den el tercer término (actualmente el nuestro es 24) y sumados den el segundo (10). <br><br>
Al dar estas condiciones podremos descubrir que estos números serán 6 y 4, los cuales acomodaremos 
en los paréntesis.<br><br>
<math class="tamfrac"><mfrac><mn>(6x+6)(6x+4)</mn>
            <mn>6</mn></mfrac></math><br><br>
Al tener finalmente la expresión buscaremos eliminar el divisor, que en este caso es el 6, 
así que lo simplificamos, esto lo haremos sacándole mitad al 6 y a un paréntesis que sea posible, 
mientras le saquemos una mitad al 6 se le sacará una mitad al paréntesis.<br><br>
<math class="tamfrac"><mfrac><mn>(6x+6)(6x+4)</mn>
            <mn>6</mn></mfrac></math><br><br>
Comenzamos sacando mitad del primer paréntesis y del 6.<br><br>
<math class="tamfrac"><mfrac><mn>(3x+3)(6x+4)</mn>
            <mn>3</mn></mfrac></math><br><br>
Notaremos que no se le puede sacar mitad ahora, pero sí tercera, así que haremos eso.<br>
<math class="tamfrac"><mfrac><mn>(x+1)(6x+4)</mn>
            <mn>1</mn></mfrac></math><br><br>
Ahora que quedó como división entre 1 tendremos la respuesta con una multiplicación de binomios.<br>
(x+1)(6x+4)
