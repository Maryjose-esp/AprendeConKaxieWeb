            <h1 id="ED">Expresión desarrollada de un número</h1>
            <P> Cuando hablamos de expresión desarrollada de un número, nos referimos a interpretar 
                mediante una suma, la cifra numérica, es importante que recuerdes el valor posicional de cada 
                dígito, ya que los estaremos utilizando para esta progresión.<br><br>
                Recuerda que: la unidad es para los valores que están en la primera posición, la décima para 
                los de la segunda posición, centenas para los números de la tercera posición, millares para la 
                cuarta, quinta y sexta posición (agregando la unidad, décima y centena según corresponda en la oración).<br>
                <center><img src="./images/progresion2img/ExpliDes.png" WIDTH="40%"></center><br>
La posición se determina leyendo el valor de izquierda a derecha, siendo el primer número izquierdo la primera posición.<br><br>
<p id="ejemED">Aplicaremos la expresión desarrollada de 1456.</p><br>
Iniciaremos separando los números, dependiendo de su posición comenzaremos a identificarlo:<br><br>
1 está en la cuarta posición, por ello es una unidad de millar (aplicando lo anterior dicho).<br><br>
4 está en la tercera posición, la posición de las centenas, siendo 4 centenas.<br><br>
5 se encuentra ubicado en la segunda posición, correspondiente a las décimas, son 5 décimas.<br><br>
Finalizamos con el 6, que se encuentra en la primera posición, la unidad, así que podemos decir que son 6 unidades.<br>
<center><img src="./images/progresion2img/EjemED.png" WIDTH="40%"></center><br>
Ahora que contamos con el valor posicional de cada número, podemos interpretar 1456 con una igualdad:<br><br>
1456= 1 unidad de millar + 4 centenas + 5 décimas + 6 unidades.<br><br>
Otra manera para representarlo es:<br><br>
1456= 1*1000 + 4*100 + 5*10 + 6*1<br><br>
De esta forma, la expresión desarrollada es:<br><br>
1456= 1000 + 400 + 50 + 6<br><br>
Cómo al igual puede ser un proceso inverso (de la expresión desarrollada ir a la estándar).<br><br>
1000 + 400 + 50 + 6 = 1456<br><br><br>

Para mayor comprensión mostraremos más ejemplos con cada forma.<BR><BR>
<big><B>284100</B></big><BR>
Para hacer la expresión desarrollada de este número nos guiaremos de su valor posicional, contando los 
dígitos que tiene después del número, por ejemplo el 2 tiene 5 dígitos después de su posición, entonces 
