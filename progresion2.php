            <h1 id="ProdN">Productos notables</h1>
            <P> Los productos notables son  expresiones algebraicas que se obtienen de un producto el cual 
                conocemos ya que sigue reglas fijas y  su resultado puede obtenerse por seguir la regla, 
                por ello no se necesita realizar la operación. Las operaciones son sencillas de recordar, 
                por ello no necesitamos realizar la multiplicación.<br><br>
            </p>
            <h3 id="CB">El cuadrado de un binomio</h3>
            <p>Un binomio es una expresión algebraica formada por 2 términos, los cuales puede tener signo igual o diferente.<br>
Cuando elevamos al cuadrado el binomio x+y equivale a multiplicar este binomio por sí mismo.<br><br>
<p class="italic">(x+y)²<br>
x+y<br>
x+y<br>
—---<br>
x²+xy<br>
    +xy+y²<br>
—------------<br>
x²+2xy+y²</p><br>
Sí el símbolo del binomio es negativo, simplemente cambia a negativo la segunda parte.<br>
Podemos observar que el cuadrado de un binomio es igual al cuadrado del primer término más/menos el doble 
producto del primer término con el segundo más el cuadrado del segundo.<br><br>
Siguiendo esta regla tendremos como ejemplo (3x+2)<br><br>
Cuadrado del primer término: <p class="italic">9x²</p>
Doble del producto del primer y segundo término: <p class="italic">2(6x)=12x</p>
Cuadrado del segundo término: 4<br>
Dando como resultado: <p class="italic">9x² + 12x + 4</p><br>
            </p>
            <br>

            <h3 id="PBC">Producto de dos binomios conjugados</h3>
            <p> Los binomios conjugados tienen un término común que se identica por llevar el mismo signo, 
                mientras que los otros términos que llevan signos contrarios se les conoce como términos simétricos.<br><br>
                <div class=P2Term><center>Términos comunes&emsp;<b>Términos semejantes</b><br>
(x<b>+y</b>)&emsp;(x<b>-y</b>)</center></div><br><br>
El producto de 2 binomios conjugados es igual al cuadrado del término común menos el cuadrado del término simétrico.<br>
Al tener en cuenta la regla podemos aplicarla con el binomio: (4x²+3y)(4x²-3y)<br>
La regla nos dice que primero se tiene el cuadrado del término común:<br>
<p class="italic">(4x²)²= 16x⁴</p><br>
Después ocupamos la resta del cuadrado del término simétrico:<br>
<p class="italic">(3y)(-3y)= -9y</p><br>
            </p>
            <br>

            <h3 id="BTC">Producto de dos binomios con término común</h3>
            <p> Los binomios de la forma (x+a)(x+b) en donde “x” es el término común y “a ” y “b” 
                son los  términos no comunes se les conoce como productos de binomios con un término común.<br><br>
La regla dice que éste es el cuadrado del término común, más o menos el producto de la suma o resta de los términos 
no comunes por el común, más/menos el producto de los términos no comunes. <br><br>
Esto lo podemos comprobar al resolver el binomio de forma tradicional:<br>
<p class="italic">x+a<br>
x+b<br>
—---<br>
x² +ax<br>
      bx + ab<br>
—-------------<br>
x²+ ax + bx + ab</p><br>
Y así confirmamos que lo que nos dice la regla es correcto.<br>
            </p>
            <br>

            <h3 id="TCP">Trinomio cuadrado perfecto (FORMA x²+bx=ca)</h3>
            <p>Recordemos que el cuadrado de un binomio es igual al cuadrado del primer término, 
                más/menos el doble producto del primer término por el segundo, más el cuadrado del segundo.<br><br>
Al producto que se obtiene al elevar un binomio al cuadrado se le conoce como trinomio cuadrado perfecto, 
ya que consta de las siguientes características:<br><br>
-Consta de tres términos<br>
-De esos términos dos tienen raíz cuadrada exacta, son cuadrados.<br><br>
-El tercer término es el doble del producto de las raíces de los términos cuadráticos, ejemplo:<br>
<p class="italic">(a+2)²= a² + 2(a)(2) + 2²</p>
<p class="italic">a² + 4a + 4</p><br>
Los términos cuadrados son a² y 4, porque ambos tienen raíz cuadrada exacta.<br><br>
Y el término 4a es el doble producto de las raíces cuadradas anteriores.<br><br>
Ejemplo de cómo completar un trinomio cuadrado perfecto:<br>
Tendremos la expresión x² + 12x= 8<br>
Para resolverlo necesitamos dividir entre 2x el segundo término, y el resultado elevarlo al cuadrado:<br>
<p class="italic">12x/2x= 6<br>
6²= 36</p>
Ahora lo acomodamos de la siguiente manera:<br>
<p class="italic">x² + 12x= 8<br>
x² + 12x + 36= 8 + 36</p><br>
            </p>
            <br>

            <h3 id="CasEsp">Caso especial multiplicación de trinomios (a+b+c)(a+b-c)</h3>
            <p>En este tipo de casos hay una regla especial, la cual dice:<br><br>
El producto de dos trinomios con dos términos positivos iguales, y un tercer término cuyo 
signo es diferente en cada trinomio, es el cuadrado del primer término, más 2 veces el producto 
del primero con el segundo, más el cuadrado del segundo, menos el cuadrado del tercero.<br><br>
Realizaremos una operación para comprobarlo:<br>
<p class="italic">(a+b+c)(a+b-c)= <br>
a² + ab - ac + ab + b² - bc + ac + bc -c² =<br>
a² + 2ab + b²  -c²</p><br>
Comprobamos que cumple con la regla.<br><br>
-Caso especial multiplicación de trinomios (a+b+c)(a-b-c)<br><br>
Primero resolveremos el binomio haciendo la operación, después comprobaremos que se adapta a la regla: <br>
<p class="italic">(a+b+c)(a-b-c)= <br>
a² - ab - ac + ab - b² - bc + ac - bc -c² =<br>
a² - b² - 2bc -c² </p><br>
La regla dice que: <br>
La multiplicación de dos trinomios con un término positivo igual, y los otros dos términos 
iguales en valor pero con signos diferentes en cada trinomio es el cuadrado del primer término, 
menos el cuadrado del segundo término, menos dos veces el segundo por el tercero, menos el 
cuadrado del tercero.<br><br>
Confirmando el resultado que obtuvimos.<br><br>
            </p>
            <br>

            <h3 id="CubSum">Cubo de la suma de dos cantidades</h3>
            <p>La regla en esta ocasión nos dice que el cubo de la suma de dos cantidades es 
                igual al cubo de la primera cantidad, más 3 seguido del cuadrado del primero 
                por el segundo, más 3 seguido del primero por el cuadrado del segundo, más 
                el cubo del segundo.<br><br>
Una forma rápida de comprobarlo es multiplicar el cuadrado del binomio por sí mismo, 
esto con la intención de terminar más rápido:<br>
<p class="italic">(a+b)² (a+b)= (a² + 2ab + b²)(a+b)=<br>
a³+ a²b + 2a²b + 2ab² + ab² + b³=<br>
a³+ 3a²b + 3ab² + b³</p><br>
Comprobamos que cumple con la regla.<br><br>
</p>
            <br>

            <h3 id="CubRes">Cubo de la resta de dos cantidades</h3>
            <p>Aplicaremos casi lo mismo que lo anterior, solamente cambiarán algunos signos:<br>
            <p class="italic">(a-b)² (a-b)= (a² - 2ab + b²)(a-b)=<br>
a³- a²b - 2a²b + 2ab² + ab² - b³=<br>
a³ - 3a²b + 3ab² - b³</p><br>
Ahora viendo la regla para este caso, el cubo de la diferencia de dos cantidades es igual al 
cubo del primer término, menos el triple del cuadrado de la primera por el segundo, más el 
triple del primero por el cuadrado del segundo, menos el cubo del segundo término, comprobando 
que tenemos el resultado correcto.<br><br>
</p><br>

