            <h1 id="ES">Expresión simplificada</h1>
            <P>Con expresión simplificada damos a entender que queremos reducir lo máximo posible 
                una ecuación. <br>
Es algo simple que como recordarás, se ha enseñado desde álgebra en secundaria.<br><br>
<br><h3 id="Sum">Suma y resta</h3>
Para recordar, empezaremos a simplificar operaciones de suma y resta con variables.<br>
Tendremos como ejemplo 12y + 14y<br>
Cuando ambos números cuentan con la misma literal solamente será cuestión de sumar sus coeficientes:<br>
<p class="italic">12 + 14= 26</p>
Y se conservará la literal “y”, siendo el resultado: <br>
<p class="italic">26y</p>
Se realizará lo mismo en caso de ser resta, simplemente restando los coeficientes.<br>
En caso de contar con dos números con distintas literales será imposible sumarlos.<br>
<br>
Veremos más ejemplos para comprender el tema.<br><br>
<b>5y - 5y²</b><br>
Aunque ambos números tengan misma literal su exponente es diferente, lo que impide que se pueda 
realizar cualquier operación de suma o resta, recordemos que solamente se pueden sumar o restar 
cuando su literal es la misma, sus exponentes también deben serlo.<br><br>
<b>2y + 3z</b><br>
Como se ha mencionado anteriormente, si los números tienen diferentes literales no es posible realizar
 una suma o resta.<br><br>
<b>7a + 4a</b><br>
Contamos con dos números con literales y exponentes iguales, podemos continuar realizando el ejercicio.<br><br>
Separamos los cocientes de la literal, dejando 7 y 4.<br><br>
Sumando ambas cantidades da 11, ahora solo se añadirá la literal, la cual no se modifica en lo 
transcurso de la operación, tenemos como resultado 11x.<br><br>
<b>8n<sup>2</sup> - 3n<sup>2</sup></b><br>
Recordando que la condición para realizar este tipo de operación es necesario que la literal y el 
exponente sean el mismo podremos continuar con la operación.<br><br>
Restamos los coeficientes:<br>
8 - 3 = 5<br><br>
Conservaremos la literal n<sup>2</sup>, dejando como resultado 5n<sup>2</sup>.<br><br>

<b>-9x -11x</b><br>
Repetiremos el mismo proceso, pero recordaremos la ley de signos, si ambos son iguales entonces se 
sumaran las cantidades y se mantendra el signo, como ambos son de signo negativo se sumarán manteniendo 
su signo, sumamos coeficientes:<br><br>
-9-11= -20<br><br>
Ahora que contamos con el valor de la suma le añadiremos la literal, obteniendo -20x como resultado.<br><br>

<h3 id="Mul">Multiplicación</h3><br>
Seguimos con la multiplicación y la división.<br>
En este caso sí podremos dividir o multiplicar las variables que contengan literales diferentes, 
para este ejemplo utilizaremos 8a*5b<br>
Multiplicaremos los coeficientes:<br>
<p class="italic">8*5= 40</p>
Y se mantienen ambas literales respetando el orden de las literales 
(solamente alfabético por el momento).<br>
<p class="italic">40ab</p><br>

Ejemplos:<br><br>
<b class="italic">3a*3a</b><br>
Cuando contamos con una multiplicación donde ambos tienen la misma literal se suman los exponentes de 
las literales, al no mostrar alguno significa que su exponente es 1.<br><br>
Así que primero realizamos la multiplicación de los coeficientes dando 3*3, dando como resultado:<br>
9<br><br>
Ahora sumaremos los exponentes, como ambos son uno su suma sería de 2 quedando como resultado:<br>
<p class="italic">9a<sup>2</sup></p><br>
 
<b class="italic">4b*3c</b><br>
En este caso contamos con dos números con literales distintas, como recordaran es posible realizar una 
multiplicación con literales diferentes, seguimos con el proceso original hasta llegar a las literales.<br><br>
Multiplicamos los coeficientes:<br>
4*3=12<br><br>
Ahora como son dos literales distintas solamente se pondrán juntas respetando su orden alfabético.<br>
<p class="italic">12bc</p><br>


<b class="italic">44a*a<sup>3</sup></b><br>
Como hemos hecho hasta el momento se multiplicarán primero los coeficientes<br><br>
44*1=44<br><br>
Se suman los exponentes de las literales, los cuales son 1 y 3 dando como resultado 4.<br><br>
El resultado sería: <p class="italic">44a<sup>4</sup></p><br>
 

<b class="italic">4x<sup>3</sup>*3y<sup>2</sup></b><br>
Seguimos aplicando el mismo proceso que al inicio, multiplicamos los coeficientes:<br>
4*3=12<br><br>
Ahora tenemos dos literales con exponentes se hará lo mismo que en el ejemplo anterior, se respetaran 
sus exponentes y su orden.<br><br>
Dando como resultado: <p class=”italic”> 12x<sup>3</sup>y<sup>2</sup> </p><br>
 

<b class="italic">6x<sup>5</sup>*x<sup>-2</sup></b><br>
Comenzamos multiplicando los coeficientes:<br>
6*1=6<br><br>
Ahora sumamos los exponentes de las literales, pero vemos un signo negativo en el exponente, respetando 
la ley de signos que dice “si hay dos números con diferente signo se restan y se mantiene el signo del 
mayor” de tal manera que restamos los exponentes de x.<br>
5-2=3 <br><br>
Manteniendo el signo del mayor.<br><br>
Ya que contamos con ambos resultados la respuesta es:<br>
<p class="italic">6x<sup>3</sup></p><br>



<h3 id="Div">División</h3><br>
Aunque las multiplicaciones pueden lograrse con distintas literales las divisiones no son ese caso, 
solamente se pueden lograr cuando las literales son iguales, pero sí se pueden realizar con diferente 
exponente.<br><br>
Por ejemplo la división entre 10x y 5x. <br><br>
Se dividirán los coeficientes.<br><br>
<math class="tamfrac"><mfrac><mn>10</mn><mn>5</mn></mfrac></math> = 2<br><br>
Y en las literales se restarían sus exponentes, como ambas tienen uno se reducirán y el resultado será 
2, sin literal.<br><br>


Ejemplos:<br><br>
10x/5y</b><br>
No es posible.<br><br>
<b>50a/5a</b><br>
Primero se dividen los coeficientes:<br>
50/5= 10<br><br>
En la multiplicación se suman los exponentes, ahora al ser división, se hará lo contrario y se restan 
los exponentes, como ambos tienen 1 se eliminan, dejando como resultado:<br>
10<br><br>

<b>20x<sup>5</sup>/2x</b><br>
Comenzamos dividiendo los exponentes: <br>
20/5=4<br><br>
Esta vez una literal cuenta con exponente distinto a uno, siendo 5, ahora podremos restar los 
exponentes:<br>
5-1=4<br><br>
El resultado sería:<br>
<p class="italic">4x<sup>4</sup></p><br>

<b>4y<sup>7</sup>/2y<sup>2</sup></b><br>
Gracias a que contamos con números con mismas literales podremos realizar la operación, iniciando con 
la división de cocientes:<br>
4/2=2<br><br>
Las literales cuentan con los exponentes 7 y 2, como recordaran estos se tendrán que restar.<br>
7-2=5<br><br>
El resultado sería:<br>
2y<sup>5</sup><br><br>

<b>30x<sup>4</sup>y/3x<sup>-2</sup>y</b><br>
Como hemos hecho en cada ejercicio ahora dividiremos los coeficientes:<br>
30/3=10<br><br>
Tenemos los exponentes de dos literales en cada número, primero restamos los de la literal “x”:<br><br>
(4) - (-2) Como se cuenta con un signo negativo se volverá positivo al estar detrás de otro signo 
negativo dando como resultado 6.<br><br>
En el caso de las “y”, ambas tienen como exponente el 1, por tal motivo se eliminan.<br><br>
El resultado es:<br> <p class=”italic”>10x<sup>6</sup></p><br>


<h3 id="PD">Propiedad distributiva</h3><br>
La propiedad distributiva es aquella que nos enseña que al estar un coeficiente fuera de un paréntesis 
(el cual tiene dentro literales y algún otro coeficiente), este multiplicará a todo lo que esté dentro de, por ejemplo:<br>
<p class="italic">36(x-3+y)</p>
Al seguir lo que nos dice, podríamos interpretarlo de la siguiente manera:<br>
<p class="italic">36*x= 36x<br>
36*¨-3= -108<br>
36*y= 36y</p>
Ahora que ya contamos con los resultados, podremos añadirlos a la ecuación correctamente.<br>
<p class="italic">36x-108+36y</p>
No olvides acomodar dicha ecuación<br>
<p class="italic">36x+36y-108</p><br>
Para finalizar con esta explicación, se realizará una ecuación con todo lo aprendido.<br>
<math class="tamfrac">
    <mfrac>
        <mn>2(-x+44)+2x</mn>
        <mn>3y</mn>
    </mfrac>
</math>
<br><br>
Primero utilizaremos la propiedad distributiva:<br>
<p class="italic">2*-x= -2x</p>
<p class="italic">2*44= 88</p>
De forma que ahora nuestra ecuación va así:<br>
<math class="tamfrac">
    <mfrac>
        <mn>-2x+88+2x</mn>
        <mn>3y</mn>
    </mfrac>
</math><br><br>
Viendo que hay dos números con misma literal, se juntan, al ser de distinto signo se restan y se obtiene 
el número del mayor, en este caso el resultado es 0<br>
Dándonos por resultado la expresión:<br>
<math class="tamfrac">
    <mfrac>
        <mn>-88</mn>
