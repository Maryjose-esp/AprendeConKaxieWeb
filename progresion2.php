término se le puede sacar raíz cuadrada y al tercero igual.<br>
√81x²= 9x<br>
√36y²= 6y<br><br>
Ahora comprobamos que el doble de la multiplicación de los nuevos términos obtenidos dará como 
resultado el segundo término del trinomio cuadrado perfecto.<br>
2(9x*6y)= 2(54xy)= 108xy<br><br>
El signo del segundo término del trinomio dirá qué signo tendrá el segundo término del binomio, 
siendo el resultado de 81x² + 108xy +36y²:<br>
(9x+6y)²<br><br>

