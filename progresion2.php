<h3 id="FacTCP">Factorización de un trinomio cuadrado perfecto (ax² ± bx + c)</h3><br>
<P><div class=P2Term> Este tipo de factorización se utiliza cuando tenemos un trinomio cuadrado en su forma ax² ± bx + c, 
mientras se cumpla que ax² y c tienen raíces cuadradas exactas, las cuales al multiplicarse entre 
ellos y por 2, dan como resultado el término medio.</div></p>
Te explicaremos paso a paso cómo factorizar 16x² - 48x + 36.<br><br>
Primero tenemos que asegurarnos que la expresión esté ordenada (que los grados de los términos 
vaya de mayor a menor), al estarlo calcularemos las raíces del primer y tercer término:<br>
√16x² = 4x<br>
√36 = 6<br><br>
Comprobamos que el doble del producto de las raíces de los anteriores términos sea igual al segundo 
término de la expresión.<br>
2(4x*6)= 2(24x)= 48x <br><br>
El signo del segundo término de la expresión determinará el signo que tendrá el segundo término en 
el binomio. La factorización del término 16x² - 48x + 36 daría como resultado:<br>
(4x-6)²<br><br>

