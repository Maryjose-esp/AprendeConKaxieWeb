            <h3 id="FC">Factor común</h3>
            <P><div class=P2Term>Este tipo de factorización normalmente se utiliza con los polinomios de dos o más 
                términos que comparten al menos un factor o variable en los coeficientes.</div><br><br>
Para factorizar la expresión primero se debe determinar el coeficiente común, usaremos como ejemplo la expresión 
<math><msup><mi>48x</mi><mn>2</mn></msup></math>
<math><msup><mi>y</mi><mn>2</mn></msup></math>z-32
<math><msup><mi>x</mi><mn>7</mn></msup></math>
<math><msup><mi>y</mi><mn>3</mn></msup></math>z<br><br>
El factor común entre el 32 y el 48 es 16 (lo obtenemos al saber el máximo común divisor entre el 32 y 48).<br><br>
Las variables comunes son “x” y “y”, la mayor potencia común de x es 2 y de la y es 3, es por esto que los factores 
comunes dentro de las variables serán <math><msup><mi>x</mi><mn>2</mn></msup></math><math><msup><mi>y</mi><mn>3</mn></msup></math>
, y así obtenemos que el factor común entre ambos números es: <br>
<p class="italic">16x²y³</p><br>
Para poder obtener la expresión factorizada debemos obtener el resultado de dividir el factor común con 
cada uno de los números de la expresión:<br><br>
<math class="tamfrac"><mfrac><mn> -32x⁷y³ </mn><mn> 16x²y³z </mn></mfrac></math>=-2x⁵z
<br><br>
<math class="tamfrac"><mfrac><mn> 48x²y⁵z </mn><mn> 16x²y³ </mn></mfrac></math>=3y²z<br><br>
Ahora podremos interpretarlo como una multiplicación, siendo esta la expresión factorizada:<br>
<p class="italic">16x²y³ (3y²z - 2x⁵z)</p><br>

<b>Más ejemplos:</b> <br>
<b class="italic">1.- 5x<sup>3</sup>y + 7x</b><br><br>
En este caso los coeficientes son números primos, de tal manera que no podemos obtener un mínimo común 
divisor, mantendremos solo las literales comunes de cada número.<br><br>
Observaremos que en ambos se tiene la literal x, así que esta sera el término común.<br>
x<br><br>
Ya que contamos con el término común vamos a dividir los números originales entre este término para 
obtener la multiplicación.<br><br>
<math class="tamfrac"><mfrac><mn>5x³y</mn>
            <mn>x</mn></mfrac></math> = 5x²y <br><br>
<math class="tamfrac"><mfrac><mn>7x</mn>
            <mn>x</mn></mfrac></math> = 7 <br><br>
Ahora podemos factorizar:<br>
x (5x<sup>2</sup>y + 7)<br><br>

<b class="italic">2.- 4x<sup>4</sup> - 12x<sup>3</sup></b><br><br>
Seguimos el mismo procedimiento, esta vez podemos obtener el mínimo común divisor de los coeficientes.<br>
El cual será 4.<br><br>
La mayor potencia común de la literal x es el 3, así que el término común sería 4x<sup>3</sup>.<br><br>
Ya que contamos con el término común lo dividiremos entre los números originales.<br><br>

<math class="tamfrac"><mfrac><mn>4x⁴</mn>
            <mn>4x³</mn></mfrac></math> = x <br><br>
<math class="tamfrac"><mfrac><mn>-12x³</mn>
            <mn>4x³</mn></mfrac></math> = -3 <br><br>
Ahora colocaremos la expresión factorizada:<br>
4x<sup>3</sup> (x - 3)<br><br>

<b class="italic">3.- 11x<sup>5</sup> + 10x<sup>2</sup>y</b><br><br>
Veremos que esta vez aunque contemos con un número divisible el otro es primo, por lo cual no 
encontraremos un mínimo común divisor, así que continuamos con las literales el término común solamente 
sería x<sup>2</sup> ya que no contamos con la literal “y” en ambos números.<br><br>

<math class="tamfrac"><mfrac><mn>11x⁵</mn>
            <mn>x²</mn></mfrac></math> = 11x³ <br><br>
<math class="tamfrac"><mfrac><mn>10x²y</mn>
            <mn>x²</mn></mfrac></math> = 10y <br><br>
Ahora ya obtendremos la expresión factorizada, que es:<br>
x<sup>2</sup> (11x<sup>3</sup> + 10y)<br><br>
