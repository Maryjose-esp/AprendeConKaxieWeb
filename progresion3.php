            <h1 id="quees">Lenguaje algebraico y natural</h1>
            <P>El lenguaje natural es el cómo nos expresamos día a día como por 
                ejemplo decir el doble de un número, de forma que en el lenguaje 
                algebraico podría ser interpretado como 2x.<br><br>
El lenguaje algebraico es una forma para traducir el lenguaje natural a símbolos y 
números, siendo esta una ventaja al momento de manejar cantidades desconocidas con 
símbolos fáciles de escribir, lo que permite simplificar expresiones.<br><br>

Pongamos de ejemplo, al decir la suma de dos números, podemos interpretarlo con dos 
variables distintas, por ser dos números los cuales desconocemos, y+x, de esta misma 
manera, hay muchas más expresiones en lenguaje natural que pueden pasar a ser del 
lenguaje algebraico, como se mostraba en la progresión 1, en esta ocasión resolveremos 
problemas en los que se necesitará hacer una transliteración del lenguaje natural al 
algebraico.<br><br>
<p id="ejemplo">Para reforzar el tema, se explicará el siguiente problema:<br><br>
Juanita estuvo planeando ir a un estreno de película, pero al estar distraída 
no se ha dado cuenta que compró dos boletos, al darse cuenta que tenía un boleto 
extra decidió revenderlo, cosa que se le complicó porque había comprado unas 
palomitas y un refresco que costó una décima parte del total, pagó un total de 
250 pesos, lo único que Juanita recuerda es que se le hizo curioso que las palomitas 
le costaron exactamente lo doble que la bebida.<br><br>
Al leer el problema, vemos que hay dos boletos de los que no sabemos el precio 2b, 
y unas palomitas de las que lo único que sabemos es que costaron el doble que la 
bebida, la cual es una décima parte del total, 250.<br><br>
Comenzamos por obtener cuánto costó la bebida<br>
250/10= 25<br><br>
Ahora podemos deducir que las palomitas son el doble que 25<br>
25*2=50<br><br>
Ahora que tenemos los precios de cada objeto podremos expresarlo así:<br>
2b+25+50=250<br><br>
De forma que podemos resolverlo<br>
2b+75=250<br>
2b=250-75<br>
2b=175<br>
b= 175/2<br>
b=87.5<br><br>
Con esto sabemos que cada boleto tiene un costo de 87.5 pesos.<br>
Esto solamente sería una operación sencilla, de igual manera comenzaremos con los ejercicios:</p><br>

