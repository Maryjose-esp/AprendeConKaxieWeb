        <div class="definition">
            <h3>Números Naturales</h3>
            <p>Los números naturales son aquellos enteros no negativos que se utilizan para contar elementos o representar cantidades de objetos.</p>
            <p>Ejemplos: 1, 2, 3, 4, 5, ...</p>
        </div>
        <div class="definition">
            <h3>Números Enteros</h3>
            <p>Los números enteros incluyen a los números naturales junto con sus negativos y el número cero.</p>
            <p>Ejemplos: -3, -2, -1, 0, 1, 2, 3, ...</p>
        </div>
        <div class="definition">
            <h3>Números Racionales</h3>
            <p>Los números racionales son aquellos que se pueden expresar como el cociente de dos enteros, donde el denominador no es cero.</p>
            <p>Ejemplos: 1/2, -3/4, 5/6, 2.5, -0.75, ...</p>
        </div>
        <div class="definition">
            <h3>Números decimales</h3>
