        <p><h2 id="mensualidad">Mensualidad:</h2>
            
            Es el pago periódico, generalmente mensual, que se realiza por un préstamo, hipoteca o servicio.
            <br>La mensualidad incluye la amortización del capital más los intereses devengados en ese período.
            <br><br>
            <b><h3 id="ejeMens">Un ejemplo sería </h3></b>
            <br>Bajo un sistema de cuotas fijas, la mensualidad se calcula así:
            <br>
            Monto del préstamo: $10,000, Plazo en meses: 12, Tasa de interés mensual: 1% = 0,01<br>
            Mensualidad = Monto del préstamo / Plazo en meses = $10,000 / 12 = $833. Entonces la mensualidad sería de $833.
            <br><br>
            <p><h3>Aquí te muestro otro ejemplo:</h3><br>
            <br>
            Juan tiene el objetivo de comprar una casa en 5 años que cuesta $100,000. Actualmente tiene $10,000 ahorrados y gana un sueldo mensual de $4,000. Si abre una cuenta de ahorros que le da un interés del 4% anual:
            <br>
            ¿Cuánto dinero deberá ahorrar mensualmente para juntar el total necesario para la casa en 5 años?
            <br>
            Dinero necesario para la casa dentro de 5 años: $100,000<br>
            Dinero actual ahorrado: $10,000<br>
            Dinero faltante: $100,000 - $10,000 = $90,000<br>
            Meses totales en 5 años: 5*12 = 60 meses<br>
        <br>
        Para calcular la mensualidad se debe de hacer así
        <br>Cálculo mensualidad: M = P / nt <br>
        Donde M es la cantidad que Juan necesita ahorrar mensualmente, P es la cantidad total que necesita ahorrar en total, 
        n es el número de veces que el interés se compone por año (en este caso, mensualmente), y t es el número de años.
        <BR>
        Usando esta fórmula, puedes calcular la cantidad que Juan necesita ahorrar cada mes para alcanzar su objetivo de comprar una casa en 5 años.
        <BR>
        En este caso, P es $90,000 (la cantidad restante después de su ahorro inicial), n es 12 (ya que el interés se compone mensualmente), y t es 5. 
        <BR>
        Por lo tanto, al sustituir estos valores en la fórmula, se obtiene:
        <BR><BR>
        <math class="tamfrac" display="block"><mrow><mi>M</mi><mo>=</mo><mfrac><mrow><mn>12</mn><mo>&#x00D7;</mo><mn>5</mn></mrow><mrow><mn>90,000</mn></mrow></mfrac><mo>=</mo><mfrac><mrow><mn>60</mn></mrow><mrow><mn>90,000</mn></mrow></mfrac><mo>=</mo><mn>1,500</mn></mrow></math>
        <BR>
        Por lo tanto, Juan necesita ahorrar $1,500 cada mes para alcanzar su objetivo de comprar la casa en 5 años.</p>
