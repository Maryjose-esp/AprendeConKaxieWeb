            <h1 id="cc">Conceptos clave <br></h1>
            <p>Punto. Suele ser representado por un círculo, no tiene dimensiones.<br>
                Recta. Conjunto infinito de puntos.<br>
                Plano. Superficie de dos dimensiones.<br>
                Espacio. Todo lo que nos rodea.<br>
                Congruencia. Dos figuras F y G son congruentes si una de ellas puede ser movida y
