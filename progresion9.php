        <h1 id="introduccion">Introduccion</h1>
        <P>Entiéndase a la geometría sintética como la rama matempatica que se encarga de estudiar
            las propiedades de aquellas figuras que existen en el espacio, esto en base a una serie
            de ideas llamados "primitivos" , y a una serie de de proposiciones que se dan por
            ciertas, siendo estas proposiciones los "postulados". Lo anterior posibilita las conclusiones
            acercade de tales figuras.</p><br>

        <p>Esta geometría se caracteriza porque inutiliza los sistemas numéricos.</p><br>

        <p>Esta geometría se relaciona con la llamada geometría euclidiana , puesto que esta última es un
            ejemplo del uso del método sintético en el estudio de las figuras.
            Para saber un poco más... Euclides trabajó en una obra titulada "Los elementos", en la cual
            establece 5 postulados y en base a ellos se estudian las propiedes de elementos unidimensionales
            como la línea y los planos, bidimiensionales como tríangulos y círculos, y tridimensionales como
            conos y esferas.
        </p><br>

        <p>Un sistema abstracto se compone de dos tipos de objetos relacionados singularmente entre ellos.
            En este tratamiento hay que tener claros 4 conceptos primitivos: punto, recta, plano y espacio.
            Dichos conceptos ya fuero explicados al principio.</p><br>

        <h1 id="postulados">Postulados</h1>
        <P>
        <ul>
            <li>Toda recta y plano son un conjunto de puntos.</li>
            <li>Dados dos puntos diferentes, hay una recta que los contiene.</li>
            <li>Dados tres puntos diferentes y no colineales, hay un plano que los contiene.</li>
            <li>Si dos puntos están en un plano, la recta que los contiene está en el plano.</li>
            <li>Si dos planos se intersecan, esa intersección es una recta.</li>
            <li>Toda recta contiene al menos dos puntos. Todo plano contiene al menos tres puntos no colineales.</li>
            <li>El espacio contiene cuatro puntos no coplanares</li>
        </ul>
        </p>
        <br>
        <h1 id="interposicion">Interposición</h1>
        <P>La interposicón de puntos es una relación indefinida que se da entre 3 puntos donde se habla
            de la posición de punto en relación a los otros 2.</p>

        <p>
        <ul>Postulados
            <li>Si B está entre A y C, entonces B está entre C y A.</li>
            <li>De tres puntos cualisquiera contenidos en una recta, uno está entre los
                otros dos.</li>
            <li>Dados cuatro puntos distintos y colineales, siempre podremos
                nombrarlos en un cierto orden A, B, C, D, de tal manera que
                A-B-C-D</li>
            <li>Dados dos puntos distintos A y B, se tiene que:
                <ul>
                    <li>1. Existe un punto C, de tal manera que A-B-C</li>
                    <li>2. Existe un punto D, de tal manera que A-D-C</li>
                </ul>
            <li>
            <li>-Si A-B-C, entonces A, B y C son tres puntos distintos en una recta.</li>
        </ul>
        </p><br>

